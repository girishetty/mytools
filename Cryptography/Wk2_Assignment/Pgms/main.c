void test_aes_cbc(const char* input, const char* key, const int inputLen) {

    UINT8 ivector[16];
    UINT8 enc_output[1000];
    UINT8 dec_output[1000];
    UINT32 outLen = 0;

    memcpy(ivector, IV, 16);

    int res = aes_cbc(input, inputLen, key, ivector, enc_output, &outLen, AES_ENCRYPT);

    memcpy(ivector, enc_output, 16);
    res = aes_cbc(enc_output + 16, outLen - 16, key, ivector, dec_output, &outLen, AES_DECRYPT);
    ascii_print("decrypted output", dec_output, outLen);

    if (memcmp(dec_output, input, inputLen) != 0 ) {
        printf("AES CBC encryption/decryption FAILED\n");
    } else {
        printf("AES CBC encryption/decryption PASS\n");
    }
}

