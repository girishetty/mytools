void hex_print(const char* what, const void* pv, int len)
{
    printf("%s (%d) is: \n", what, len);
    const unsigned char * p = (const unsigned char*)pv;
    if (NULL == pv)
        printf("NULL");
    else
    {
        size_t i = 0;
        for (; i<len;++i)
            //printf("0x%02X ", *p++);
            printf("%02X ", *p++);
    }
    printf("\n");
}

void ascii_print(const char* what, const void* pv, int len)
{
    printf("%s (%d) is: \n", what, len);
    const unsigned char * p = (const unsigned char*)pv;
    if (NULL == pv)
        printf("NULL");
    else
    {
        size_t i = 0;
        for (; i<len;++i)
            printf("%c", *p++);
    }
    printf("\n");
}


